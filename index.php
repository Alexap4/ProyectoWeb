	<p>Anivia pronto descubrió un aliado en Ashe, Arquera de Hielo. Ashe también creía en la unidad como final de la eterna lucha de Fréljord y Anivia ofreció a la líder de la tribu su ayuda. Ahora, con la guerra a la vista en el horizonte, Anivia se prepara para luchar por la paz, pero conoce la verdad inevitable de su destino. Algún día, el mal se alzará desde el hielo, y ella deberá aniquilarlo, a cualquier precio.</p>
